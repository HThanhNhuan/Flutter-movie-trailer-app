/// Defines the different visual styles that can be applied to a MovieCard.
enum MovieCardStyle {
  /// Default style with no extra effects.
  normal,

  /// A static, subtle blue glow. Used for "Now Playing".
  staticGlow,

  /// A slow, pulsing amber glow. Used for "Top Rated".
  pulsingGlow,

  /// A static, mysterious gradient overlay. Used for "Upcoming".
  gradientOverlay,
}
